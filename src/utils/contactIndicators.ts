/**
 * Вычисляет процент заливки иконок на основе количества дней контакта
 * Строго пропорционально: days / 7
 * @param days - общее количество уникальных дней с контактом (0-7)
 * @returns процент заливки от 0 до 1
 * 
 * Шкала заполнения (строго пропорционально):
 * - 0 дней: 0% (0/7)
 * - 1 день: ~14% (1/7)
 * - 2 дня: ~28% (2/7)
 * - 3 дня: ~42% (3/7)
 * - 4 дня: ~57% (4/7)
 * - 5 дней: ~71% (5/7)
 * - 6 дней: ~85% (6/7)
 * - 7 дней: 100% (7/7)
 */
export function getContactFillPercentage(days: number): number {
  const clampedDays = Math.max(0, Math.min(7, Math.floor(days)));
  return clampedDays / 7;
}

